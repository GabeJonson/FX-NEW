						The foreign exchange market (also known as Forex or FX) is a global market for currency trading. The Forex market is the largest and most liquid market in the world, with average traded values that can be trillions of dollars per day.
						<br><br>
						The main participants in Forex are the largest international banks. Financial centers around the world function as mediators between a wide range of buyers and sellers 24 hours a day, except to weekends. The currencies are traded worldwide among the major financial centers that are located in London, New York, Tokyo, Zürich, Frankfurt, Hong Kong, Singapore, Paris and Sydney.
						<br><br>
						The foreign exchange market is involved in currency conversion. For example, it permits a business in the United States to import goods from European Union, and pay Euros, even though its income is in United States dollars.
						<br><br>
						4 major currency pairs are usually traded on the market: Euro against US dollar (EUR/USD), US dollar against Japanese yen (USD/JPY), British pound against US dollar (GBP/USD), and US dollar against Swiss franc (USD/CHF).
						<br><br>
						Forex is very popular with individual traders due to its accessibility and its simplicity relative to other markets. One can trade Forex in an online account from almost anywhere in the world. Forex traders buy or sell a currency hoping that it will rise or fall against the value of another in order to profit from the difference in price.
						<br><br>
						Forex traders have frequent opportunities to take advantage of price fluctuations due to the high volumes of trade activity and the many buyers and sellers in the market. In Forex prices are affected by macroeconomic data such as a decision made by a central bank. In Forex a person simply needs to evaluate whether one country&#39;s currency will be worth more or less than the currency of another.
